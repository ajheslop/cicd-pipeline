class Calculator:
    """return string"""
    def add(num1, num2):
        """return string"""
        return num1 + num2

    def subtract(num1, num2):
        """return string"""
        return num1 - num2

    def divide(num1, num2):
        """return string"""
        return num1 / num2

    def multiply(num1, num2):
        """return string"""
        return num1 * num2
